"""Утилиты для работы с БД, не привязанные к конкретной модели."""


def escape_like(query_str: str) -> str:
    """Экранирует спецсимволы LIKE/ILIKE (`%`, `_`, `\\`) для безопасного поиска.

    Без этого юзер мог бы передать "100%" и матчить любую строку,
    а "_test" — любую строку из 5+ символов.

    Используется в паре с `.ilike(..., escape="\\")`.

    Порядок replace важен: сначала экранируем сам бэкслеш, иначе
    задвоим уже экранированные спецсимволы.
    """
    return (
        query_str
        .replace("\\", "\\\\")
        .replace("%", "\\%")
        .replace("_", "\\_")
    )